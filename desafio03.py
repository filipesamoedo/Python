# Desafio 3
# Crie um script Python que leia dois números e teste mostrar a soma entre eles.

# Solicitação ao usuário dos dois números

num1 = float(input('Digite o primeiro número:'))
num2 = float(input('Digite o segundo número:'))

# Impressão com cálculo

print('A soma entre os dois números é:', num1 + num2)

# Avançando com os operadores matemáticos

print('A subtração entre os dois números é:', num1 - num2)

print('A multiplicação entre os dois números é:', num1 * num2)

print('A divisão entre os dois números é:', num1 / num2)
